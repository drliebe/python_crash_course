glossary = {
    'variable': 'stores a value',
    'list': 'stores a series of ordered values',
    'set': 'stores a set of unique values',
    'dictionary': 'stores a set of key-value pairs',
    'for loop': 'iterates a fixed number of times',
    'if statement': 'a conditional test that only executes if true',
    'and statement': 'a way to combine two logical statements',
    'or statement': 'a second way to combine two logical statements',
}

for key, value in glossary.items():
    print(str(key) + ' means: ' + str(value))